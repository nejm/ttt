package com.smi.model;

/**
 *
 * @author Nejm
 */
public class Message {
    
    private String from;
    private String to;
    private String body;

    public Message() {
    }
    
    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
    
}
